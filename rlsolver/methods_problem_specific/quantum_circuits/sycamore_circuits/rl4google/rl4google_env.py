            '''calculate the multiple and avoid repeat'''
            ct_dimss = dims_tens[env_is, node_i0s] + dims_tens[env_is, node_i1s] * if_diffs.unsqueeze(1)
            ct_bools = bool_tens[env_is, node_i0s] | bool_tens[env_is, node_i1s]
