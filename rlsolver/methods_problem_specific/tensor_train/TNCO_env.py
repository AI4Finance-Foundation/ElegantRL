def get_nodes_list_and_band_edges_of_tensor_ring(len_list: int = 4):
    ban_edges = len_list

    nodes = [[] for _ in range(len_list)]  # 
    for i in range(len_list):
        nodes[i].append((i - 1) % len_list)
